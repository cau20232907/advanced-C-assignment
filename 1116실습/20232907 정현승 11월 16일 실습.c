void NOV30() {
	int size;
	int* arr = NULL, total = 0;
	scanf("%d", &size);
	arr = calloc(size, sizeof(int));
	if (!arr) {
		puts("MEMORY ERROR OCCURED");
		return;
	}
	for (int* p = arr; p < arr + size; p++)
		scanf("%d", p);
	for (int* p = arr; p < arr + size; p++)
		total += *p;
	printf("total: %d\navg: %.2f", total, total * 1.0 / size);
	free(arr);
}

void NOV24() {
	student* p = NULL, * q = NULL;
	p = calloc(3, sizeof(student));
	if (!p) return;
	for (q = p; q < p + 2; q = q->next)
		q->next = q + 1;
	q = p;
	q->kor = 100;
	q->eng = 90;
	q++;
	q->kor = 80;
	q->eng = 70;
	q++;
	q->kor = 60;
	q->eng = 50;
	for (q = p; q; q = q->next)
		printf("%d %d\n", q->kor, q->eng);
	//화살표 3개 사용도 가능
	free(p);
}



